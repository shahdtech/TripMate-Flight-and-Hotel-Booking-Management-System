    void setRowData(String[] rowData) {
        HID.setText(rowData[0]);
        hotelName.setText(rowData[1]);
        basePrice = Double.parseDouble(rowData[4]);
        hotelPrice.setText(Double.toString(basePrice));
        updataPrice();

    }

    private void updataPrice() {
        //CHANGE THE PRICE BASED IN TYPE
        double price = basePrice;

        if (roomType.getText().equals("Standard Room")) {
            price += 0;
        } else if (roomType.getText().equals("Double Room")) {
            price += 200.0;
        } else if (roomType.getText().equals("Suite")) {
            price += 500.0;
        } else if (roomType.getText().equals("Penthouse Suite")) {
            price += 1000.0;
        }
        hotelPrice.setText(Double.toString(price));
    }

