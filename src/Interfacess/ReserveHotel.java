    public void saveToFile() {

        
        try (FileWriter writer = new FileWriter("Reserve hotel.txt")) {

            writer.write("\n");
            writer.write("\n");
            writer.write("                   Hotel ID:: " + HID.getText() + "\n");
            writer.write("------------------------------------------------------" + "\n");
            writer.write("||  Hotel :      " + hotelName.getText() + "               Room Type :    " + roomType.getText() + " ||");
            writer.write("\n||  Check-In :" + CID.getText() + "               Check-out:      " + COD.getText() + " ||");
            writer.write("\n||                  Total price: " + hotelPrice.getText() + "   ||");
            writer.write("\n------------------------------------------------------" + "\n");

        } catch (IOException e) {
            e.printStackTrace();
        }

    }

