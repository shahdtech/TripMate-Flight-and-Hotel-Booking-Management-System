    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:

        //GET THE STRING AND STORE IT INTO VARIABLE
        String Source = Sbox.getSelectedItem().toString();
        String Dest = Dbox.getSelectedItem().toString();
        

        try {

            con = DriverManager.getConnection("jdbc:mysql://localhost:3306/TripMate", "root", "Sh@123456");
            //MAKE QUERY TO SEARCH BUTTON AND EXECUTE IT BY PREPARE STATEMENT
            String query1 = "SELECT * FROM Flight WHERE Source = ? AND Destination = ?";
            Pst = con.prepareStatement(query1);
            Pst.setString(1, Source);
            Pst.setString(2, Dest);
            ResultSet rs1 = Pst.executeQuery();

            //CLEAR OLD DATA
            DefaultTableModel df = (DefaultTableModel) jTable2.getModel();
            df.setRowCount(0);

            //ADD RECORD INTO TABLE 
            while (rs1.next()) {
                int flightId = rs1.getInt("FlightID");
                String sour = rs1.getString("Source");
                
                String dest = rs1.getString("Destination");
                String fromTime = rs1.getString("From");
                String toTime = rs1.getString("To");
                double price = rs1.getDouble("Price");

                df.addRow(new Object[]{flightId, sour, dest, fromTime, toTime, price});
            }

        } catch (SQLException ex) {
            System.out.println("SQL statement is not executed!");
        }
        


    }//GEN-LAST:event_jButton1ActionPerformed
