    private void jTable2MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable2MouseClicked
        // TODO add your handling code here:
        DefaultTableModel df = (DefaultTableModel) jTable2.getModel();
        int SelectIndex = jTable2.getSelectedRow();
        if (SelectIndex != -1) {
            int columnCount = df.getColumnCount();
            String[] rowData = new String[columnCount];
            for (int i = 0; i < columnCount; i++) {
                rowData[i] = df.getValueAt(SelectIndex, i).toString();
            }

            BookFlight BF = new BookFlight();
            BF.setRowData(rowData);
            BF.setVisible(true);
            dispose();
        } else {
            JOptionPane.showMessageDialog(this, "Please Choose Flight ");
        }


    }//GEN-LAST:event_jTable2MouseClicked

